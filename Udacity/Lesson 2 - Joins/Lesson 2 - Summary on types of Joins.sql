/*
INNER JOINS
these return rows that appear on both tables. this is like a cross-section between two tables.

a venn diagram can be used to represent these JOINS
the overlapping middle section shows the rowas for which the on clause is true. one table is selected as a left table and the other is selected as a RIGHT

TYPES OF JOINS
- LEFT JOIN
syntax

SELECT columns
FROM left table
LEFT JOIN right table

the result set will give all the results that match with the right table as well as any results in the left table that did not match
- RIGHT JOIN
- FULL OUTER JOIN
